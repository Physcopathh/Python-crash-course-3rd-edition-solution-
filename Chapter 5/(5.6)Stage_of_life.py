age = 65

if age < 2:
    print('The person is Baby.')
elif 2 <= age < 4:
    print('The person is Toddler.')
elif 4 <= age < 13:
    print('The person is Kid.')
elif 13 <= age < 20:
    print('The person is Teenager.')
elif 20 <= age < 65:
    print('The person is Adult.')
else:
    print('The person is Expired')